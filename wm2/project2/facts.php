  <h1>Facts About Chicago</h1>
  <ul>
    <li>The two blue stripes on the Chicago flag represent the two branches of the Chicago River.</li>
    <li>The Chicago River actually flows out of Lake Michigan. The flow of the river was reversed in 1900 to prevent sewage dumped into the river from polluting the city's water supply, Lake Michigan.</li>
    <li>The Chicago River is dyed green every year for Saint Patrick's Day. The dye powder is orange before being mixed with the water.</li>
    <li>The Chicago Water Tower is one of only a few buildings to survive the Great Chicago Fire.</li>
    <li>Chicago O'Hare International Airport along with a strip of land connecting it to the rest of Chicago was annexed by the city in the 1950s.</li>
    <li>The name Chicago comes from the Miami-Illinois word “shikaakwa,” meaning “smelly onion” due to the number of wild onions that grew on the banks of the Chicago River. </li>
  </ul>
