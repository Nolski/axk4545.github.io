  <h1>Government of Chicago</h1>
  <p>
  The government of Chicago is a mayor-council government with a 50 
  member council of aldermen, each representing one of the 50 wards in the city.
  The current mayor is Rahm Emanuel and a list of the current aldermen can be found 
  <a href="http://www.cityofchicago.org/city/en/about/wards.html">here</a>.
  The current city clerk is Susana A. Mendoza. 
  She assumed office on May 16, 2011 and is currently(as of September 2016) running 
  for Comptroller of Illinois in the 2016 Special Election. The current city treasurer is Kurt Summers.
  He has been in office since December 1, 2014.
  </p>
  
