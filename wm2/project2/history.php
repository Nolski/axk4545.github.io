  <ul class="history">
    <li><b>1885:</b> As part of rebuilding after the fire, Chicago constructs the world's first skyscraper.</li>
    <li><b>May 4th, 1886:</b> In response to the deaths of two workers at a strike the day before, a demonstration is held in Haymarket Square. When police arrive to end the demonstration, a bomb is thrown killing 7 police officers and 4 civilians.</li>
    <li><b>June 21st, 1886:</b> 8 men stand trial for the Haymarket bombing deaths. All are convicted though none for throwing the bomb. 4 will be executed on November 11, 1887. Of the others two had their sentences changed to life in prison, one was sentenced to 15 years and the last committed suicide in jail.</li>
    <li><b>1889:</b> Jane Addams opens Hull House providing social services to immigrants and the poor.</li>
    <li><b>May 1st - October 30th, 1894:</b> Chicago hosts the World's Columbian Exposition. It is held on lake fill land created with the debris from the Chicago Fire.</li>
    <li><b>January 2nd, 1900:</b> The Chicago Sanitary and Ship Canal is completed replacing the Illinois and Michigan Canal and allowing the flow of the Chicago River to be reversed.</li>
    <li><b>1919:</b> The 18th Amendment to the United States Constitution bans the production and sale of alcohol. This leads to the rise of gangsters and bootleggers such as Al Capone and Bugs Moran.</li>
    <li><b>February 14th, 1929:</b> Gang tensions between rivals Al Capone and Bugs Moran result in the St. Valentine's Day Massacre when Capone sends men to gun down members of Moran's gang.</li>
    <li><b>August 26th - 29th, 1968:</b> Protests at the Democratic National Convention culminate in violent clashes between protesters and Chicago Police and Illinois National Guardsmen. Many of the protesters are severely beaten and injured. Protesters in Grant Park are tear gassed and beaten when they refuse to disperse. Connecticut senator Abraham Ribicoff characterizes the violence as "gestapo tactics" in a speech at the convention.</li>
    <li><b>1974:</b> The Sears Tower is constructed holding the title of world's tallest building until 1998.</li>
    <li><b>1979:</b> Jane Byrne becomes the city's first female mayor.</li>
    <li><b>1983:</b> Harold Washington becomes Chicago's first African-American mayor.</li>
    <li><b>1989:</b> Richard M. Daley becomes mayor following in his father, Richard J. Daley's footsteps.</li>
    <li><b>March 30th, 2003:</b> Mayor Daley orders the demolition of Meig's Field without authorization. Six Xs are bulldozed into the single runway stranding planes.</li>
    <li><b>May 16th, 2011:</b> Rahm Emanuel is sworn in as mayor.</li>
  </ul> 
