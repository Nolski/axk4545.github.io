  <h1>Chicago History</h1>
  <p>Chicago has a rich history as a hub of trade and commerce in the Midwest. Below is a timeline of some key events in the history of Chicago.</p>
  
  <ul class="history">
    <li><b>1784-1788:</b> Fur trader Jean Baptiste Point du Sable becomes the first permanent resident of the area that is now Chicago.</li>
    <li><b>1803:</b> The United States Army establishes Fort Dearborn at what is now the intersection of Wacker Drive and Michigan Avenue.
    The fort was burned by the Potowatami during the War of 1812.</li>
    <li><b>August 12th, 1833:</b> The town of Chicago is organized.</li>
    <li><b>March 4th, 1837:</b> The City of Chicago is incorporated.</li>
    <li><b>May 1837:</b> William Butler Ogden is elected Chicago's first mayor.</li>
    <li><b>1848:</b> The Illinois and Michigan Canal opens connecting the Mississippi River with Lake Michigan and the Great Lakes. Construction of the Galena and Chicago Union Railroad begins. It will later become the Chicago and Northwestern Railroad.</li>
    <li><b>1855:</b> In response to cholera outbreaks the previous summer, the city begins construction of a sewer system. The street levels will be raised in 1859 to accomodate the sewers.</li>
    <li><b>May 18th, 1860:</b> Abraham Lincoln is nominated as the Republican presidential candidate at the convention in Chicago.</li>
    <li><b>October 8th, 1871:</b> The Great Chicago Fire starts around 9PM and burns until early on the morning of the 10th. Over the course of the two days, the fire destroyed 3.3 square miles of the city, leaving 100,000 people homeless and resulting in 300 deaths.</li>
  </ul>
